/**
 * 中国儿童身高标准
 *
 * 0~6岁（0~81月）：WS/T 423—2022《7岁以下儿童生长标准》
 *   表 B.3 男童年龄别身长/身高标准差数值
 *   表 B.4 女童年龄别身长/身高标准差数值
 *   注：2岁以下为仰卧身长，2岁起为立位身高
 *
 * 7~18岁（84~216月）：WS/T 612—2018《7~18岁儿童青少年身高发育等级评价》
 *   表 A.1 男生身高发育等级划分标准数值
 *   表 A.2 女生身高发育等级划分标准数值
 *
 * 来源：中华人民共和国卫生行业标准（国家卫生健康委员会）
 *
 * 百分位计算方法：
 *   原始数据为 -2SD/-1SD/中位数/+1SD/+2SD
 *   基于正态分布对以下百分位进行线性插值：
 *     P3  ≈ -2SD
 *     P10 ≈ -2SD + 0.72 × (-1SD - (-2SD))   [z = -1.28]
 *     P25 ≈ -1SD + 0.326 × (M - (-1SD))      [z = -0.674]
 *     P50 = 中位数 M
 *     P75 ≈ M + 0.674 × (+1SD - M)           [z = +0.674]
 *     P90 ≈ +1SD + 0.28 × (+2SD - +1SD)      [z = +1.28]
 *     P97 ≈ +2SD
 */

import { GrowthStandardFile, GrowthRow } from '@/types/growth';

/** 从 SD 值计算 7 条百分位（正态分布近似插值） */
function fromSD(
  n2: number, n1: number, m: number, p1: number, p2: number,
): Pick<GrowthRow, 'p3' | 'p10' | 'p25' | 'p50' | 'p75' | 'p90' | 'p97'> {
  const r = (v: number) => Math.round(v * 10) / 10;
  return {
    p3:  r(n2),
    p10: r(n2 + 0.72  * (n1 - n2)),
    p25: r(n1 + 0.326 * (m  - n1)),
    p50: r(m),
    p75: r(m  + 0.674 * (p1 - m)),
    p90: r(p1 + 0.28  * (p2 - p1)),
    p97: r(p2),
  };
}

